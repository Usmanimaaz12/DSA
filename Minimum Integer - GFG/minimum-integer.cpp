class Array
{
public:
    template <class T>
    static void input(vector<T> &A,int n)
    {
        for (int i = 0; i < n; i++)
        {
            scanf("%d ",&A[i]);
        }
    }

    template <class T>
    static void print(vector<T> &A)
    {
        for (int i = 0; i < A.size(); i++)
        {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends

class Solution {
  public:
    # define ull unsigned long long int 
    int minimumInteger(int N, vector<int> &A) {
        ull S=accumulate(A.begin(),A.end(),0ll);
        int x=ceil(S*1.0/N),ans=INT_MAX;
        for(int num:A)
        {
            ans=num>=x&&num<ans?num:ans;
        }
        return ans;
    }
};

